const waybackDate = document.getElementById('wayback-date');
const waybackReset = document.getElementById('wayback-reset');
const waybackBanner = document.getElementById('wayback-banner');
const mainList = document.getElementById('main-list');
const originalMainItems = Array.from(mainList.querySelectorAll(':scope > li'));

function updateTopColors() {
  document.querySelectorAll('.rank-gold, .rank-silver, .rank-bronze').forEach(li => {
    li.classList.remove('rank-gold', 'rank-silver', 'rank-bronze');
  });
  const visible = Array.from(mainList.querySelectorAll(':scope > li:not(.wayback-hidden)'));
  if (visible[0]) visible[0].classList.add('rank-gold');
  if (visible[1]) visible[1].classList.add('rank-silver');
  if (visible[2]) visible[2].classList.add('rank-bronze');
}

function restoreMovedItems() {
  document.querySelectorAll('li.wayback-promoted').forEach(li => li.remove());
  document.querySelectorAll('li.wayback-hidden').forEach(li => li.classList.remove('wayback-hidden'));
  originalMainItems.forEach(li => mainList.appendChild(li));
}

function applyWayback(dateStr) {
  restoreMovedItems();

  if (!dateStr) {
    waybackReset.style.display = 'none';
    waybackBanner.style.display = 'none';
    return;
  }

  let hiddenCount = 0;
  document.querySelectorAll('#main-list > li, #legacy-list > li').forEach(li => {
    if (li.dataset.added > dateStr) {
      li.classList.add('wayback-hidden');
      hiddenCount++;
    }
  });

  let promotedCount = 0;
  const promotions = [];
  document.querySelectorAll('#legacy-list li[data-id]').forEach(li => {
    const history = HISTORY[li.dataset.id];
    if (!history) return;
    const entry = history.find(h => h.from <= dateStr && dateStr <= h.to);
    if (!entry) return;
    promotions.push({ li, entry });
  });

  promotions.sort((a, b) => a.entry.rank - b.entry.rank);

  promotions.forEach(({ li, entry }) => {
    const clone = li.cloneNode(true);
    clone.classList.remove('wayback-hidden', 'open');
    clone.classList.add('wayback-promoted');
    clone.querySelector('.box-main').addEventListener('click', function() {
      clone.classList.toggle('open');
    });

    const visibleItems = Array.from(mainList.querySelectorAll(':scope > li:not(.wayback-hidden)'));
    const insertBefore = visibleItems[entry.rank - 1] || null;
    mainList.insertBefore(clone, insertBefore);
    li.classList.add('wayback-hidden');
    promotedCount++;
  });

  waybackReset.style.display = 'inline-block';
  waybackBanner.style.display = 'block';
  let msg = `Viewing list as of ${dateStr}.`;
  if (hiddenCount > 0) msg += ` ${hiddenCount} level${hiddenCount > 1 ? 's' : ''} added after this date are hidden.`;
  if (promotedCount > 0) msg += ` ${promotedCount} legacy level${promotedCount > 1 ? 's' : ''} restored to their historical rank. Dashed lines indicate current legacy levels`;
  waybackBanner.textContent = msg;
  updateTopColors();
}

waybackDate.addEventListener('change', function() {
  applyWayback(this.value);
});

waybackReset.addEventListener('click', function() {
  waybackDate.value = '';
  applyWayback('');
  updateTopColors();
});

updateTopColors();
